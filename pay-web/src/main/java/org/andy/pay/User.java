public class User {
    private int age;
    private String name;

    public int getAge() {
        return age;
    }

    public User() {
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
